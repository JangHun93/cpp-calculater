#include <iostream>
using namespace std;

int main()
{
	int FirstNumber = 0;
	int SecondNumber = 0;

	cin >> FirstNumber;
	cin >> SecondNumber;

	cout << "A + B = " << FirstNumber + SecondNumber << endl;
	cout << "A - B = " << FirstNumber - SecondNumber << endl;
	cout << "A * B = " << FirstNumber * SecondNumber << endl;
	cout << "A / B = " << FirstNumber / SecondNumber << endl;
}